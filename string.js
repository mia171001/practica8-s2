// Definimos una cadena de texto
const text = "   ¡Hola, Alejandra!   ";

// Método length: Obtenemos la longitud de la cadena (número de caracteres)
const longText = text.length;
console.log("Longitud de la cadena:", longText);

// Método trim(): Eliminamos los espacios en blanco al inicio y al final de la cadena
const textWithoutSpaces = text.trim();
console.log("Texto sin espacios:", textWithoutSpaces);

// Método toUpperCase(): Convertimos todos los caracteres de la cadena a mayúsculas
const textCapital = text.toUpperCase();
console.log("Texto en mayúsculas:", textCapital);

// Método toLowerCase(): Convertimos todos los caracteres de la cadena a minúsculas
const textLowercase = text.toLowerCase();
console.log("Texto en minúsculas:", textLowercase);


